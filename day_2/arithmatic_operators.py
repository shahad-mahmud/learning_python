# + -> addition operator 
# - -> subtraction operator 
# * -> Multiplication operator 
# / -> Dividation operator 
# // -> integer division or floor division
# % -> modulus operator (remainder) 
# ** -> Exponent operator (power)

num1 = 50
num2 = 7

print('number 1: {} \t number 2: {}'.format(num1, num2))

add = num1 + num2
print('Addition:', add)

subtract = num1 - num2
print('Subtraction:', subtract)

mul = num1 * num2
print('Multiplication:', mul)

div = num1 / num2
print('Dividation:', div)

floor_div = num1 // num2
print('Floor dividation:', floor_div)

mod = num1 % num2 
print('Modulus:', mod)

expo = 2 ** 4
print('Exponent (2^4):', expo)